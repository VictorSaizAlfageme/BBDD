FROM EMPLE
GROUP BY DEPT_NO;

--SOLUCION:
SELECT E.DEPT_NO, D.DNOMBRE,SUM(E.SALARIO+NVL(E.COMISION_PCT,0)) PRESUPUESTO
FROM EMPLE E, DEPART D 
WHERE E.DEPT_NO = D.DEPT_NO
GROUP BY E.DEPT_NO, D.DNOMBRE   
HAVING SUM(E.SALARIO+NVL(E.COMISION_PCT,0)) IN 
        (SELECT MAX(SUM(S.SALARIO+NVL(S.COMISION_PCT,0)))
          FROM EMPLE S
          GROUP BY DEPT_NO );
                        
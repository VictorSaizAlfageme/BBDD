SELECT E.DEPT_NO , D.DNOMBRE
FROM DEPART D , EMPLE E
WHERE D.DEPT_NO = E.DEPT_NO
AND UPPER(E.OFICIO) = 'PROGRAMADOR'
GROUP BY E.DEPT_NO, D.DNOMBRE
HAVING COUNT(*) = (SELECT MAX(COUNT(*))
                            FROM EMPLE 
                            WHERE UPPER(OFICIO) = 'PROGRAMADOR'
                            GROUP BY DEPT_NO);

